// –î–ª—è —Ä–µ–Ω–¥–µ—Ä–∏–Ω–≥–∞ (–æ—Ç–æ–±—Ä–∞–∂–µ–Ω–∏—è) –æ–Ω–∏ –ø–µ—Ä–µ–¥–∞—é—Ç—Å—è –≤ jsx –æ–±—ä–µ–∫—Ç —Å –ø–æ–º–æ—â—å—é –º–µ—Ç–æ–¥–∞ `.render()`:
