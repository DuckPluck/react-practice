// –≠–ª–µ–º–µ–Ω—Ç—ã –∏–º–º—É—Ç–∞–±–µ–ª—å–Ω—ã - –ø–æ—Å–ª–µ —Å–æ–∑–¥–∞–Ω–∏—è –Ω–µ–ª—å–∑—è –º–µ–Ω—è—Ç—å –∞—Ç—Ä–∏–±—É—Ç—ã –∏ –¥–∞–∂–µ –ø–æ—Ç–æ–º–∫–æ–≤.
// –ß—Ç–æ–±—ã –æ–±–Ω–æ–≤–∏—Ç—å –∏–Ω—Ç–µ—Ä—Ñ–µ–π—Å, –ø—Ä–∏–¥–µ—Ç—Å—è —Å–æ–∑–¥–∞—Ç—å –Ω–æ–≤—ã–π —ç–ª-—Ç –∏ –ø–µ—Ä–µ–¥–∞—Ç—å –µ–≥–æ –≤ `root.render()`(üëé) –∏–ª–∏ –∏–∑–º–µ–Ω–∏—Ç—å –µ–≥–æ —Å–æ—Å—Ç–æ—è–Ω–∏–µ(üëç)
function clock() {
  const element = (
      <div>
        <h1>Hello, world!</h1>
        <h2>It is {new Date().toLocaleDateString()}.</h2>
      </div>
  );
  root.render(element);
}

